    void jugarAhora() {
        if (txtNombreJugador.getText().length() < 4)
        {

            new AlertaValidacion().setVisible(true);

        } else
        {
            DatosJuego datos = DatosGenerales.pares;
            datos.setJugador(txtNombreJugador.getText());

            this.setVisible(false);
            datos.setNumeroPares(5);
            new PanelJuego().setVisible(true);
        }

    }

